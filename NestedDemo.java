class NestedDemo 
{ 
  static class Nested 
  { 
// Declaration of the main method inside a static nested class. 
    public static void main(String[] args) 
     { 
         System.out.println("Static nested main method"); 
     } 
   } 
public static void main(String[] args) 
 { 
   System.out.println("Outer class main method"); 
 } 
}